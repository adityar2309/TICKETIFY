<template>
<div id="#app">
<h1>Admin Delete Movie</h1>
<form @submit.prevent="deletemovie">
    <input class="form-control" type="text" placeholder="name" v-model="name" disabled>
    <input class="form-control" type="text" placeholder="genre" v-model="genre" disabled>
    <input class="form-control" type="text" placeholder="rating" v-model="rating" disabled>
    <button class="btn btn primary " >Delete Movie</button>
</form>
</div>
</template>
<script>
import axios from 'axios'
export default{
    name: "DeleteMovie",
    props: ["id"],
    data() {
        return {
            movies_id: this.$route.params.id,
            name: "",
            genre: "",
            rating: ""
        }
    },
    methods: {
        async deletemovie(){
            let token =  localStorage.getItem("token")
            const response = await axios.post("deletemovie/" + this.movies_id, {
                name: this.name,
                genre: this.genre,
                rating: this.rating
        }, { headers: {Authorization: "Bearer " + token
                    }});
        if (response.data.message == "success") {
            console.log("deletemovie successful")
            console.log(response)
            this.$router.push('/admin');
        } else {
            console.log(response)
            console.log("deletemovie failed")
        }
    },
    async getmovie(){
        const response = await axios.get("getmovie/" + this.movies_id);
        if (response.data.message == "success") {
            this.name = response.data.movie.name
            this.genre = response.data.movie.genre
            this.rating = response.data.movie.rating
            console.log("getmovie successful")
            console.log(response)
        } else {
            console.log(response)
            console.log("getmovie failed")
        }
    }
    },
    mounted(){
        this.getmovie()
    }
}

</script>
